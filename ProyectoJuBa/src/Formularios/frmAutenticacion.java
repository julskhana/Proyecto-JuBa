    private void btIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btIngresarActionPerformed
        // TODO add your handling code here:        
        String cuenta = tfUsuario.getText();
        String clave = String.valueOf(pfClave.getPassword());
        
        ConexionBD c = new ConexionBD();
        try {
            c.conectar();
            usuario u = new usuario(cuenta, clave);
            if(c.esUsuarioValido(u)){
                //olvidar clave  de usuario activo en sistema
                u = c.obtenerDatosUsuario(tfUsuario.getText());
                JOptionPane.showMessageDialog(null,"Bienvenido "+u.getNombres()+" "+u.getApellidos()+".","Autenticaci칩n",JOptionPane.INFORMATION_MESSAGE);
                System.out.println("Usuario correcto");
                //cargando datos del usuario autenticado
                frmSeleccionarEmpresas se = new frmSeleccionarEmpresas(u);
                se.setVisible(true);
                this.dispose();
                u.setClave(null);
            }else{
                JOptionPane.showMessageDialog(null,"Usuario o Clave Incorrectos","Autenticaci칩n",JOptionPane.ERROR_MESSAGE);                        
                System.out.println("Usuario o clave incorrecto");
                limpiar();
            }
            c.desconectar();
        }catch (Exception e){
            System.out.println(e);
        }
        
    }//GEN-LAST:event_btIngresarActionPerformed

